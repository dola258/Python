b = "가나다라마" #CharSequence
print(len(b)) # 5
print("="*50)

print(b[0])  # 가 0번째 자리
print(b[4])  # 마 4번째 자리
print(b[-1]) # 마 뒤에서 첫번째부터

print(b[0:3])      # 가나다     0자리부터 3번자리 전까지
print(b[0:])       # 가나다라마 0자리부터 어딘지모를 끝까지
print(b[:3]+'...') # 가나다...  시작(0자리)부터 3번자리 전까지하고 뒤에 ...




